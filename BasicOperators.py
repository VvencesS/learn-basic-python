### Arithmetic operrators
# The addition, subtraction, multiplication, and division operators
number = 1 + 2 * 3 / 4.0
print(number)
print("----------")
# The modulo (%) operator
remainder = 11 % 3
print(remainder)
print("----------")
# Two multiplication symbols
square = 7 ** 2
cubed = 2 ** 3
print(square, cubed)
print("----------")

### Using operators with Strings
# Concatenating strings with the addition operator
str = "hello" + " " + "world"
print(str)
print("----------")
# Multiplying strings
lotsOfHellos = "hello" * 10
print(lotsOfHellos)
print("----------")

### Using operators with lists
# Be joined with the addtion operators
evenNumber = [2, 4, 6]
oddNumber = [1, 3, 5]
allNumber = oddNumber + evenNumber
print(allNumber)
print("----------")
# Using the multiplication operator
print([1, 2, 3] * 10)